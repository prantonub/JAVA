package LoopingJava;

public class DoWhile {
    public static void main(String[] args) {
        int i =1;
        do{
            System.out.println("Bangladesh "+i);
            i++;
        }while(i<=10);
    }
}
/*
What is the difference between while and do while loop?
=> The do while execute at least one time, even if the condition false. & 
=> If the condition false while loop not execute.
 */