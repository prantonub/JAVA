package assignnmentOperator;

public class AssignmentOperator {
    public static void main(String[] args) {
            int x = 3;
    int y = 2;
    x+=y; //x= 3+2 =5
    System.out.println("x = "+x);
    x-=y; //x= 5-2 =3
    System.out.println("x = "+x);
    x*=y;// x = x*y
    System.out.println("x = "+x);
    x/=y;// x = x/y
    System.out.println("x = "+x);
    x%=y;// x = x%y
    System.out.println("x = "+x);
    
    }
}
